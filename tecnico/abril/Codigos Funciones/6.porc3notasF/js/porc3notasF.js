const porcNota = function(pnota1, pnota2, pnota3){
    let nota1 = pnota1;
    let nota2 = pnota2;
    let nota3 = pnota3;
    let porc1 = nota1 * 0.3;
    let porc2 = nota2 * 0.3;
    let porc3 = nota3 * 0.4;
    let total = porc1 + porc2 + porc3;
    return total;
    return porc1;
    return porc2;
    return porc3;

}